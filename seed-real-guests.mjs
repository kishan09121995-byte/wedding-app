async function seedGuests() {
  console.log('🚀 Seeding real guests...\n');

  try {
    // First, delete all existing guests
    console.log('🗑️  Clearing old guest data...');
    const { error: deleteError } = await supabase
      .from('guests')
      .delete()
      .neq('id', 0);

    if (deleteError) throw deleteError;
    console.log('✅ Old data cleared\n');

    // Combine all guests
    const allGuests = [...brideGuests, ...groomGuests];
    console.log(`📝 Seeding ${allGuests.length} guests (${brideGuests.length} Bride + ${groomGuests.length} Groom)...\n`);

    // Seed in batches
    for (let i = 0; i < allGuests.length; i += 50) {
      const batch = allGuests.slice(i, i + 50);

      const guestBatch = batch.map((g) => ({
        name: g.name,
        city: g.city,
        side: g.side,
        pax_total: g.pax,
        jain_pax: 0,
        rsvp_status: 'Not Decided',
        f1: 'No',
        f2: 'No',
        f3: 'No',
        f4: 'No',
        room_needed: false,
        hotel_id: null,
        room_category: 'Standard',
        check_in: '2026-06-21',
        check_out: '2026-06-22',
        qr_token: Math.random().toString(36).substring(2, 15),
      }));

      const { data, error } = await supabase
        .from('guests')
        .insert(guestBatch)
        .select();

      if (error) throw error;

      console.log(`✅ Batch ${Math.floor(i / 50) + 1}: ${batch.length} guests seeded`);
    }

    console.log('\n✨ All guests seeded successfully!');
    console.log(`\n📊 Summary:`);
    console.log(`   Bride side: ${brideGuests.length} guests`);
    console.log(`   Groom side: ${groomGuests.length} guests`);
    console.log(`   Total pax (Bride): ${brideGuests.reduce((sum, g) => sum + g.pax, 0)}`);
    console.log(`   Total pax (Groom): ${groomGuests.reduce((sum, g) => sum + g.pax, 0)}`);
    console.log(`   Grand Total: ${allGuests.length} guests\n`);
  } catch (err) {
    console.error('❌ Error:', err.message);
  }
}

seedGuests();
